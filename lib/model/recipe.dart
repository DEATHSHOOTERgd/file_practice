class Recipe{
  String tipo, dificultad, nombre, ingredientes, calorias, pasos, tiempo, elaboracion, imagen;

  Recipe(this.tipo, this.dificultad, this.nombre, this.ingredientes, this.calorias, this.pasos, this.tiempo, this.elaboracion, this.imagen);

get Tipo => this.tipo;
get Dificultad => this.dificultad;
get Nombre => this.nombre;
get Ingredientes => this.ingredientes;
get Calorias => this.calorias;
get Pasos => this.pasos;
get Tiempo => this.tiempo;
get Elaboracion => this.elaboracion;
get Imagen => this.imagen;
}